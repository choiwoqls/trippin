public class C_InquiryVO {

	
	private int c_inquiry_seq;
	private int inquiry_seq;
	private int kind;
	private int purchase_no;
	
	public C_InquiryVO() {
	}

	public int getC_inquiry_seq() {
		return c_inquiry_seq;
	}

	public void setC_inquiry_seq(int c_inquiry_seq) {
		this.c_inquiry_seq = c_inquiry_seq;
	}

	public int getInquiry_seq() {
		return inquiry_seq;
	}

	public void setInquiry_seq(int inquiry_seq) {
		this.inquiry_seq = inquiry_seq;
	}

	public int getKind() {
		return kind;
	}

	public void setKind(int kind) {
		this.kind = kind;
	}

	public int getPurchase_no() {
		return purchase_no;
	}

	public void setPurchase_no(int purchase_no) {
		this.purchase_no = purchase_no;
	}
	
	
	

}
